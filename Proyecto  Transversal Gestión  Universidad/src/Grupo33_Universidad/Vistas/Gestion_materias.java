    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int option = JOptionPane.showConfirmDialog(null,"¿Eliminar "+txtNombre.getText() +"?", "¿Desea eliminar esta materia?",JOptionPane.OK_CANCEL_OPTION);
        
        if (option==JOptionPane.OK_OPTION){
            mateD.eliminarMateria(parseInt(txtCodigo.getText()));
        }else if (option== JOptionPane.CANCEL_OPTION){
            JOptionPane.showMessageDialog(null,"Eliminacion cancelada");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        String nombre = txtNombre.getText();
        int anio = Integer.parseInt(txtAnio.getText());
        boolean estado;
        if(rbdEstado.isSelected()== true){
            estado=true;
        }else{
            estado=false;
        }
         Materia mate=new Materia(nombre,anio,estado);
        mateD.guardarMateria(mate);
        
        txtCodigo.setText("");
        txtNombre.setText("");
        txtAnio.setText("");
    
    }//GEN-LAST:event_btnGuardarActionPerformed

