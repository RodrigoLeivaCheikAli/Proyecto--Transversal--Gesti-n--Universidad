    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        
        
        boolean estado;      
        Materia mateEncontrada = mateD.buscarMateria(parseInt(txtCodigo.getText()));
        if(mateEncontrada != null){
            txtCodigo.setText(mateEncontrada.getIdMateria() +"");
            txtNombre.setText(mateEncontrada.getNombre());
            txtAnio.setText(mateEncontrada.getAnioMateria()+"");
            if (mateEncontrada.isActivo()){
                 estado = true;
            }else{
                 estado=false;
            }
            rbdEstado.setSelected(estado);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


